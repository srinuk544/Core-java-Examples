package com.operators;

public class ArthamaticOperatorExample {
	public static void main(String[] args) {
		int a = 175, b = 4;
		System.out.println((double)a / b);

		
		
		
		int i = 10, x = 4;
		int c = i % x;
		System.out.println(c * 2);

		
		
		int z = 8, y = 3;
		System.out.println(z / y + z % y);

	}

}
